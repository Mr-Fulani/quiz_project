    echo "‚ùå –û—à–∏–±–∫–∞: —Å—Ç–∞—Ç–∏—á–µ—Å–∫–∏–µ —Ñ–∞–π–ª—ã –Ω–µ —Å–æ–±—Ä–∞–Ω—ã"
